package in.co.basic;

public class PrePost {

	public static void main(String[] args) {
		int a = 10;
		System.out.println(a);
		System.out.println("prePost inc...");
		System.out.println(a++);//10 (11)
		System.out.println(++a);
		
		System.out.println("prePost dec...");
		System.out.println(a--);//10 (11)
		System.out.println(--a);
		
		
		

	}

}
